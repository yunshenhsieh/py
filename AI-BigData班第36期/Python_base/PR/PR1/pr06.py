'''
請使用選擇敘述撰寫一程式，要求使用者輸入購物金額，購物金額需大於8,000（含）以上，
並顯示折扣優惠後的實付金額。購物金額折扣方案如下表所示：
金額	折扣
8,000（含）以上	9.5折
18,000（含）以上	9折
28,000（含）以上	8折
38,000（含）以上	7折
'''
#ch5 & ch6
m = eval(input())

if m>38000:
    print('%.1f' % (m*0.7))
elif m>28000:
    print('%.1f' % (m*0.8))
elif m>18000:
    print('%.1f' % (m*0.9))
elif m>8000:
    print('%.1f' % (m*0.95))
else:
    print('%.1f' % (m) )
